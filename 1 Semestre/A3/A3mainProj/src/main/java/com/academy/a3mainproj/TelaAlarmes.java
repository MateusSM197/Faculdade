    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        int idHabito = cbbIdHabito.getSelectedIndex();
        String hora = String.valueOf(cbbHora.getSelectedItem());
        String minuto = String.valueOf(cbbMinuto.getSelectedItem());
        String horaToque = String.format("%s:%s:00", hora, minuto);
        try{
            DAO dao = new DAO();
            Alarme alarme = new Alarme(idHabito, horaToque);
            dao.inserirAlarme(alarme);
            JOptionPane.showMessageDialog(null, "Alarme inserido com sucesso!");
            obterAlarmes();
            cbbIdHabito.setSelectedIndex(0);
            cbbIdAlarme.setSelectedIndex(0);
            cbbHora.setSelectedIndex(0);
            cbbMinuto.setSelectedIndex(0);
        }
        
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Falha t√©cnica, tente mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void cbbIdHabitoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbIdHabitoActionPerformed
        // TODO add your handling code here:
        HabitoAlarme fks = (HabitoAlarme) cbbIdHabito.getSelectedItem();
        txtIdHabito.setText(Integer.toString(fks.getFk_habito_id()));
        
        try{
            DAO dao = new DAO();
            Alarme[] alarme = dao.obterAlarme(fks.getFk_habito_id());
            cbbIdAlarme.setModel(new DefaultComboBoxModel<>(alarme));
        }
        
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Falha t√©cnica, tente mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_cbbIdHabitoActionPerformed

    private void cbbIdAlarmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbIdAlarmeActionPerformed
        // TODO add your handling code here:
        Alarme alarme = (Alarme) cbbIdAlarme.getSelectedItem();
        txtIdAlarme.setText(Integer.toString(alarme.getId()));
        String[] horaToque = alarme.getHoraToque().split(":");
        int hora = Integer.parseInt(horaToque[0].trim());
        int minuto = Integer.parseInt(horaToque[1].trim());
        cbbHora.setSelectedItem(hora);
        cbbMinuto.setSelectedItem(minuto);
    }//GEN-LAST:event_cbbIdAlarmeActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtIdAlarme.getText());
        int idHabito = cbbIdHabito.getSelectedIndex();
        String hora = String.valueOf(cbbHora.getSelectedItem());
        String minuto = String.valueOf(cbbMinuto.getSelectedItem());
        String horaToque = String.format("%s:%s:00", hora, minuto);
        try{
            DAO dao = new DAO();
            Alarme alarme = new Alarme(id, idHabito, horaToque);
            dao.atualizarAlarme(alarme);
            JOptionPane.showMessageDialog(null, "Alarme atualizado com sucesso!");
            obterAlarmes();
            txtIdAlarme.setText("");
            txtIdHabito.setText("");
            cbbIdHabito.setSelectedIndex(0);
            cbbIdAlarme.setSelectedIndex(0);
            cbbHora.setSelectedIndex(0);
            cbbMinuto.setSelectedIndex(0);
        }
        
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Falha t√©cnica, tente mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        int escolha = JOptionPane.showConfirmDialog(null, "Deseja deletar este alarme?");
        if(escolha == JOptionPane.YES_OPTION){
            try{
                int id = Integer.parseInt(txtIdAlarme.getText());
                int idHabito = Integer.parseInt(txtIdHabito.getText());
                Alarme alarme = new Alarme(idHabito, id);
                DAO dao = new DAO();
                dao.removerAlarme(alarme);
                JOptionPane.showMessageDialog(null, "H√°bito removido com sucesso!");
                obterAlarmes();
                cbbIdHabito.setSelectedIndex(0);
                cbbIdAlarme.setSelectedIndex(0);
                cbbHora.setSelectedIndex(0);
                cbbMinuto.setSelectedIndex(0);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null, "Falha t√©cnica. Tente novamente mais tarde.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

